        <h4 class="h4-responsive font-weight-bold">Daños derivados del trabajo <small>(accidente de trabajo y
            enfermedad profesional)</small></h4>
        <p>
          <strong>Accidente de trabajo</strong>
          Se entiende por accidente de trabajo toda lesión corporal que el trabajador
          sufra con ocasión o a consecuencia del trabajo que ejecuta
          por cuenta ajena.<br>
          Esta definición legal se refiere tanto a las lesiones que se producen en el
          centro de trabajo como a las producidas en el trayecto habitual entre éste y
          el domicilio del trabajador. Estos últimos son los considerados accidentes in
          itinere o en desplazamiento (cuando vamos de nuestro domicilio al trabajo
          y viceversa).
          <br><br>
          <strong>Enfermedad profesional</strong>
          Art. 84: la Ley General de la Seguridad Social la define como toda enfermedad
          contraída a consecuencia del trabajo ejecutado por cuenta
          ajena, en las actividades que se especifiquen en el cuadro del Real
          decreto 1299/2006 y las que se aprueben por las disposiciones de
          aplicación y desarrollo de esta ley, que estén provocadas por la acción
          de los elementos o sustancias que en dicho cuadro se indiquen
          para toda enfermedad profesional.<br>
          Esta pérdida de salud es consecuencia de lesiones de tipo orgánico o funcional
          que se desarrollan muy lentamente, de manera que el trabajador
          se siente realmente enfermo después de un período muy dilatado, a veces
          quince o veinte años de trabajo.
        </p>
