        <h4 class="h4-responsive font-weight-bold">Tipología de peligros <small>(materiales y inmateriales)</small></h4>
        <p>Quedan calificados en seis posibles niveles o grados, según
          la calificación del riesgo, en uno de estos seis niveles el empresario deberá
          llevar a cabo una acción preventiva concreta y en un período temporal
          determinado, a saber:
          <ul>
            <li>
              <strong>Riesgo trivial.</strong> No se requieren acciones específicas y puede trabajarse
              en un nivel de riesgo aceptable.
            </li>
            <li>
              <strong>Riesgo muy bajo.</strong> No se necesita mejorar la acción preventiva. Es suficiente
              con efectuar comprobaciones periódicas para asegurar que se mantiene
              la eficacia de las medidas de control implantadas.
            </li>
            <li>
              <strong>Riesgo medio.</strong> Se deben hacer esfuerzos para reducirlo determinando
              las inversiones a realizar que sean precisas para conseguir la reducción del
              mismo. Las medidas para reducir el riesgo deben implantarse en un período
              determinado.
            </li>
            <li>
              <strong>Riesgo alto.</strong> Estará asociado a consecuencias significativamente dañinas,
              por lo que se necesitará una acción precisa para reducir las probabilidades
              de daño o como base para determinar la necesidad de mejora de
              las medidas de control ya efectivamente implantadas.
            </li>
            <li>
              <strong>Riesgo muy alto.</strong> No debe comenzarse el trabajo hasta que se haya
              reducido el riesgo. Puede que se precisen recursos considerables para controlar
              el riesgo. Cuando el riesgo corresponda a un trabajo que se está
              realizando, debe remediarse el problema en un tiempo inferior al de los
              riesgos calificados como altos.
            </li>
            <li>
              <strong>Riesgo inaceptable.</strong> No debe comenzarse ni continuar el trabajo hasta
              que se reduzca el riesgo. Si no es posible reducir el riesgo, incluso con
              recursos ilimitados, debe prohibirse el trabajo.
            </li>
          </ul>
        </p>
