        <h4 class="h4-responsive font-weight-bold">Principios preventivos</h4>
        <p>La Ley 31/1995 de Prevención de Riesgos Laborales determina el marco
          por el que han de regirse las empresas en todo lo relacionado con la prevención
          de riesgos, teniendo, entre otros objetivos, el de promover la seguridad
          y la salud de los trabajadores mediante la aplicación de medidas
          y el desarrollo de las actividades necesarias para tal fin. <br>
          La ley define los principios generales de la acción preventiva (art. 15) que
          deberá aplicar el empresario y que son:<br>
          <ul>
            <li>
              Evitar los riesgos.
            </li>
            <li>
              Evaluar los riesgos que no se puedan evitar.
            </li>
            <li>
              Combatir los riesgos en su origen.
            </li>
            <li>
              Adaptar el trabajo a la persona.
            </li>
            <li>
              Planificar la prevención.
            </li>
            <li>
              Adoptar medidas que antepongan la protección colectiva frente
              a la personal.
            </li>
            <li>
              Dar las debidas instrucciones a los trabajadores.
            </li>
            <li>
              Permitir la participación de los trabajadores.
            </li>
            <li>
              Informar y consultar a los trabajadores.
            </li>
            <li>
              Dar formación teórica y práctica suficiente y adecuada en materia
              preventiva.
            </li>
          </ul>
        </p>
