        materiales (instrumentos, equipos, instalaciones) y organizativos (tiempo
        de personas o departamentos). <br><br>
        Para facilitar su elaboración y aplicación, se diferencian, en general, dos
        tipos de actividades:<br> <br>
        <ul>
          <li>
            Actividades de implantación de medidas correctoras. La evaluación
            de riesgos debe dar lugar siempre a una serie de medidas correctoras o
            preventivas que el empresario deberá planificar necesariamente para garantizar
            que los riesgos detectados se van a ir controlando en unos plazos
            razonables.<br>
            Evidentemente, la planificación de estas medidas se hará con unos criterios
            de prioridad en función de la importancia de los riesgos detectados. Esta
            planificación es la que se llama de medidas correctoras o de control del
            riesgo.
          </li>
          <li>
            Actividades de desarrollo de la acción preventiva. Los procesos de
            prevención contenidos en el Plan de prevención se planifican de forma
            independiente a las medidas correctoras. En esta planificación deberán incluirse
            las actividades por medio de las cuales se pretende conseguir los
            objetivos fijados para cada año.
