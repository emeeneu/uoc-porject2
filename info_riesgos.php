        <h4 class="h4-responsive font-weight-bold">Condiciones de trabajo</h4>
        <p>Cuando consideramos a la persona en su medio o actividad laboral, la
          situamos en unas condiciones de trabajo, es decir, rodeado de una serie
          de elementos y circunstancias propias de esa condición.<br>
          Estas condiciones de trabajo suponen, con frecuencia, riesgos potenciales,
          de ahí la necesidad de conocerlas para así determinar mejor los riesgos a
          que está expuesta la persona en su actividad diaria.<br>
          Estas condiciones de trabajo se clasifican en:<br>
          <ul>
            <li><strong>Mecánicas</strong>: aquí hemos de ver todos los elementos, materiales, utensilios
              y equipos del trabajo: el local y su estructura, las instalaciones, las
              máquinas, sus movimientos, las energías como la eléctrica, la dinámica
              o la térmica; la inercia, las cargas, presiones, tensiones y pesos; las
              herramientas, los materiales que se trabajan, el mobiliario, ropa, etc.</li>
            <li>
              <strong>Físicas</strong>: el espacio del lugar de trabajo, los accesos y tránsitos, la luz,
              el ruido, la temperatura, la humedad, la altitud, la presión, las vibraciones,
              las radiaciones.
            </li>
            <li>
              <strong>Químicas</strong>: debemos tener en cuenta aquí la presencia de cualquier
              sustancia, ya sea en estado sólido, líquido o gaseoso: el aire y su contenido
              de oxígeno o cualquier otra materia, sea o no contaminante,
              las partículas suspendidas como polvos, fibras o nieblas, los productos
              que se manejan o se producen en el proceso de trabajo: pinturas,
              disolventes, medicamentos, plaguicidas agrícolas, tintes, aceites, etc.
              que se generan como consecuencia de humos, vapores, residuos tóxicos...
              Hemos de considerar estas sustancias químicas bajo dos aspectos: primero,
              porque pueden causar incendios o explosiones por sus características
              de volatilidad, inflamabilidad y reacciones potencialmente
              peligrosas. Y segundo, porque pueden provocar enfermedades en las
              personas expuestas.
            </li>
            <li>
              <strong>Biológicas</strong>: cualquier agente vivo que pueda afectar a la persona en su
              trabajo de forma directa o indirecta. La presencia de hongos, bacterias
              o virus pueden causar enfermedades psíquicas.
            </li>
            <li>
              <strong>Psicológicas</strong>: en el trabajo hay condiciones que pueden influir sobre la
              mente de forma directa o indirecta. Unas veces, porque hay personas
