        <h4 class="h4-responsive font-weight-bold">Riesgos <small>(conceptos de peligro, riesgo laboral y factores de
            riesgo)</small></h4>
        <p><strong>Concepto de peligro</strong>: fuente o situación con capacidad de daño
          en términos de lesiones, daños a la propiedad, daños al medio
          ambiente o una combinación de ellos.<br> <br>
          <strong>Riesgo laboral</strong>: posibilidad de que un trabajador
          sufra un determinado daño derivado del trabajo. <br><br>
          <strong>Factores de riesgo</strong>: En los accidentes de trabajo podemos tener en cuenta, fundamentalmente,
          varios factores para su consideración: <br>
          <ul>
            <li>
              La probabilidad, que se analizará teniendo en cuenta la doble vertiente
              de: probabilidad de que ocurra un accidente, probabilidad de que se
              produzcan lesiones.
            </li>
            <li>
              La gravedad o severidad de los daños, que atenderá a la calificación
              de lesiones, para lo que se tendrán en cuenta las consecuencias que se
              prevea pueda provocar, o el número de trabajadores a los que puede
              afectar.
            </li>
            <li>
              La frecuencia con la que se repita el riesgo o el tiempo de exposición
              a que el trabajador o trabajadores estén expuestos
            </li>
          </ul>
        </p>
